# Write a function called product which takes in two numbers
# and returns the product of the numbers.

# Example pass throughs with function call results:

# product(2, 2) # 4
# product(2, -2) # -4


def product(a, b):
    """Return product of a and b.

        >>> product(2, 2)
        4

        >>> product(2, -2)
        -4
    """

    return a * b